/*
    从键盘输入一些字符，逐个把它们送到磁盘上去，直到输入一个#为止。
*/
#include <stdio.h>
#include <stdlib.h>

int main(int argc, char const *argv[])
{
    FILE *fp = NULL;
    char filename[25];
    char ch;

    printf("输入你要保存到的文件的名称：\n");
    gets(filename);
    if ((fp = fopen(filename, "w")) == NULL) {
        printf("error: cannot open file!\n");
        exit(0);
    }
    printf("你现在可以输入你要保存的一些字符，以#结束：\n");
    getchar();
    while ((ch = getchar()) != '#') {
        fputc(ch, fp);
    }

    fclose(fp);
    system("pause");

    return 0;
}
